void solve() {
    int n;
    cin >> n;
    vector<int> a(n + 1), z;
    vector<bool> vis(n + 1);
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
        if (a[i]) vis[a[i]] = 1;
        else z.pb(i);
    }
    if (z.size() == 1) {
        if (!vis[z[0]]) a[z[0]] = z[0];
    }
    int l = -1, r = -1;
    for (int i = 1; i <= n; ++i) {
        if (a[i] != i) {
            if (l == -1) l = i;
            else r = i;
        }
    }
    if (l == -1) cout << 0 << "\n";
    else cout << r - l + 1 << "\n";
}

