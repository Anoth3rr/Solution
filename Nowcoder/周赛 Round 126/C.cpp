void init() {}

void solve() {
    int n, k;
    cin >> n >> k;
    if ((n - k) & 1) {
        cout << -1 << endl;
        return;
    }
    for (int i = 2; i <= k + 1; ++i)
        cout << i << " ";
    for (int i = 0; i < n - k; ++i)
        cout << 1 << " ";
    cout << endl;
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    init();

    int t = 1;
    // cin >> t;
    for (int _ = 1; _ <= t; ++_) {
        solve();
    }

    return 0;
}