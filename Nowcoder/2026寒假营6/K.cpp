void init() {
}

void solve() {
    ll m, n, z;
    cin >> m >> n >> z;
    int x = z / (m + n) * (m + n);
    if (x == z) {
        cout << 1;
    } else {
        if (x + m >= z)
            cout << 0;
        else
            cout << 1;
    }
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    init();

    int t = 1;
    cin >> t;
    cout << fixed << setprecision(15);
    for (int _ = 1; _ <= t; ++_) {
        solve();
    }

    return 0;
}